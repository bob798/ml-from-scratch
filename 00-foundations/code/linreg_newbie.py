"""
============================================================
 线性回归 · 纯手写梯度下降  (Python 新手友好版)
============================================================
 目标:找一条直线 y = w*x + b 拟合数据,模型自己把 w 和 b 学出来
 全程不用任何框架,只用学过的:函数、斜率/求导、沿斜率下坡

 读这份代码前,先认识几个 Python 写法:
   - 行尾不写分号,一行就是一句
   - 没有大括号 {},用"缩进"(行首的空格)表示代码属于谁,缩进必须对齐
   - 变量不用先声明类型,  x = 3  直接写,Python 自己知道它是整数
   - def 开头是定义函数,后面跟冒号 :,下面缩进的部分就是函数体
   - 井号 #  后面是单行注释,不会被执行
   - 像本段这样用三个引号包起来的,是多行说明文字(文档字符串)
============================================================
"""

import numpy as np
# import 是"引入一个工具库"。numpy 是做数值/数组计算的库
# as np 给它起个简称,后面写 np 就代表 numpy,少打几个字


# ============================================================
#  【可调实验区】改这两个值,观察不同现象
# ============================================================
LR = 0.05          # 学习率:每步迈多大。试试 0.05(稳) / 0.5(爆炸) / 0.001(太慢)
STEPS = 200        # 训练多少步
# 全大写的变量名是一种约定,表示"这是个常量,别乱改",但 Python 不强制,改了照样能跑


# ============================================================
#  1. 造一批假数据
# ============================================================
np.random.seed(0)
#   设定随机种子。只要种子一样,每次生成的随机数就完全相同
#   作用:保证你和别人(或下次)跑出的结果一致,方便对照

x = np.linspace(-3, 3, 50)
#   np.linspace(起点, 终点, 个数):生成一串等距的数
#   这里生成 50 个从 -3 到 3 均匀分布的数,结果是一个"数组"(一长串数)

y = 2 * x + 5 + np.random.randn(50) * 0.5
#   真相是 y = 2x + 5,但故意加一点噪声让数据更像真实世界
#   2 * x  这里很关键:x 是 50 个数的数组,2 * x 会自动把每个数都乘 2
#          —— 不用写循环挨个乘,这叫"向量化",是 numpy 最爽的地方
#   np.random.randn(50):生成 50 个随机数(围绕 0 上下波动),当噪声
#   模型并不知道真相是 2 和 5,要靠训练自己找出来


# ============================================================
#  2. 模型(就是一个函数 f(x) = w*x + b)
# ============================================================
w = 0.0    # 起点故意给错(真相应是 2),看模型能不能自己爬到正确值
b = 0.0    # 起点故意给错(真相应是 5)
#   写成 0.0 而不是 0,是想让它一开始就是小数(浮点数)

def f(x):              # 定义一个函数,名叫 f,接收一个输入 x
    return w * x + b   # 缩进的这一行是函数体:返回 w*x+b
                       # 函数到这里结束(靠缩进判断,不需要任何结束符号)


# ============================================================
#  3. 损失函数 MSE(均方误差):衡量"函数好不好"
# ============================================================
def loss():
    return np.mean((f(x) - y) ** 2)
    #   f(x) - y :预测值减真实值,两个数组逐个相减,得到每点的误差
    #   ** 2     :对每个误差求平方(** 是乘方符号,2**3 就是 2 的 3 次方=8)
    #   np.mean(...):求这一串数的平均值
    #   合起来 = 把每点误差平方后求平均,这个数越小说明拟合得越好


# ============================================================
#  4. 训练循环:算斜率 → 沿斜率下坡 → 重复
# ============================================================
print(f"开始: w={w:.3f}  b={b:.3f}  loss={loss():.3f}")
#   字符串前面加 f,叫 f-string,可以把变量直接塞进 {} 里
#   {w:.3f} 表示把 w 显示成 3 位小数,比如 0.000

for step in range(1, STEPS + 1):
    #   range(1, 201) 生成 1,2,3,...,200 这一串数(包含 1,不包含 201)
    #   for step in ... 意思是:让 step 依次等于这串数里的每一个,循环执行下面缩进的代码

    pred = f(x)            # 用当前的 w、b 算出所有预测值(数组)
    err  = pred - y        # 误差 = 预测 - 真实(数组逐个相减)

    # --- 链式法则:算 loss 对 w、对 b 的斜率(梯度) ---
    grad_w = np.mean(2 * err * x)   # 2*err 是外层导数(loss=误差²求导),x 是内层(wx 对 w 求导)
    grad_b = np.mean(2 * err * 1)   # 对 b 来说,内层求导得 1,所以乘 1

    # --- 沿斜率下坡:梯度下降的核心一行 ---
    w -= LR * grad_w        # w -= A 是 w = w - A 的简写。斜率为正就减小 w(往左),为负就增大 w(往右)
    b -= LR * grad_b

    if step % 20 == 0 or step == 1:
        #   % 是取余数,step % 20 == 0 表示"每 20 步"。这里控制每 20 步打印一次,别刷屏
        print(f"第{step:>3}步: w={w:.3f}  b={b:.3f}  grad_w={grad_w:.3f}  loss={loss():.4f}")
        #   {step:>3} 表示数字右对齐、占 3 个字符宽,只是为了打印整齐好看

print(f"\n学完: w={w:.3f}  b={b:.3f}")   # \n 表示换行
print(f"真相: w=2.000  b=5.000   ← 和真相对一下")
print(f"(若 loss 一路飙升变成天文数字 = 学习率太大,发散爆炸了,把 LR 调小)")
